    let currentLang = localStorage.getItem('foodflow.lang') || 'es';
    function applyLang(lang) {
      currentLang = lang;
      localStorage.setItem('foodflow.lang', lang);
      document.documentElement.lang = lang;
      document.body.classList.toggle('lang-es', lang === 'es');
      document.body.classList.toggle('lang-en', lang === 'en');
      document.body.classList.add('lang-change');
      window.clearTimeout(window.__langChangeTimer);
      window.__langChangeTimer = window.setTimeout(() => {
        document.body.classList.remove('lang-change');
      }, 220);
      const dict = I18N[lang];
      document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key] !== undefined) el.textContent = dict[key];
      });
      document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (dict[key] !== undefined) el.setAttribute('placeholder', dict[key]);
      });
      document.querySelectorAll('[data-i18n-aria-label]').forEach(el => {
        const key = el.getAttribute('data-i18n-aria-label');
        if (dict[key] !== undefined) el.setAttribute('aria-label', dict[key]);
      });
      document.querySelectorAll('.lang button').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
      });
      document.title = 'FoodFlow';
    }
    document.querySelectorAll('.lang button').forEach(btn => {
      btn.addEventListener('click', () => applyLang(btn.dataset.lang));
    });
    applyLang(currentLang);

    /* ============ NAV SCROLL ============ */
