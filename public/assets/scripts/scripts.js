    /* ============ REVEAL ============ */
    const io = new IntersectionObserver((entries) => {
      entries.forEach(e => {
        if (e.isIntersecting) {
          e.target.classList.add('in');
          io.unobserve(e.target);
        }
      });
    }, { threshold: .12 });
    document.querySelectorAll('[data-reveal]').forEach(el => io.observe(el));
